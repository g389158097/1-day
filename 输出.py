#格式化输出
name=input('name')
job=input('job')
hometown=input('hometown')
age=input('age')
#麻烦
info='''
---------info of %s---------
name     %s
job      %s
hometown     %s
age       %s
----------end-------
'''%(name,name,job,hometown,age)
print(info)
#%是连接符
#%s代表字符串 s代表string
#%d代表digit
#%f代表float
